// Helper function to get menu items by category
export function getMenuByCategory(category: DrinkCategory): MenuItem[] {
  return MENU_ITEMS.filter(item => item.category === category);
}

// Helper function to get featured/popular items
export function getFeaturedItems(): MenuItem[] {
  return MENU_ITEMS.filter(item => item.tags?.includes('popular'));
}

// Helper function to get new items
export function getNewItems(): MenuItem[] {
  return MENU_ITEMS.filter(item => item.isNew);
}

// Helper function to get seasonal items
export function getSeasonalItems(): MenuItem[] {
  return MENU_ITEMS.filter(item => item.isSeasonal);
}
